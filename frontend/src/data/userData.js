// Function to calculate booking counts dynamically from history data
export const getUserBookingCount = (userId) => {
  // Import historyData dynamically to avoid circular imports
  const { historyData } = require('./historyData')

  // Count completed bookings for this user
  const userBookings = historyData.filter(booking =>
    booking.userId === userId && booking.status === 'completed'
  )

  return userBookings.length
}